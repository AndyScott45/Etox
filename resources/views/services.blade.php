  <p class="runway">High blood pressure (hypertension) is a common condition in which the long-term force of the blood against your artery walls is high enough that it may eventually cause health problems, such as heart disease.</p><p>Blood pressure is determined both by the amount of blood your heart pumps and the amount of resistance to blood flow in your arteries. The more blood your heart pumps and the narrower your arteries, the higher your blood pressure. A blood pressure reading is given in millimeters of mercury (mm Hg). It has two numbers.</p> <p class="runway"><ul>
  <li class="vapor"><strong>Top number (systolic pressure).</strong> The first, or upper, number measures the pressure in your arteries when your heart beats.</li><li class="vapor"><strong>Bottom number (diastolic pressure).</strong> The second, or lower, number measures the pressure in your arteries between beats.</li></ul></p>
  <h2 class="orange">Symptoms</h2>  
  <p class="ozone"> Most people with high blood pressure have no signs or symptoms, even if blood pressure readings reach dangerously high levels.</p>
  <p class="ozone">A few people with high blood pressure may have headaches, shortness of breath or nosebleeds, but these signs and symptoms aren't specific and usually don't occur until high blood pressure has reached a severe or life-threatening stage.</p> 
  <h2 class="orange">Causes</h2> 
  <p class="ozone">There are two types</p>
  <h2 class="orange">Primary (essential) hypertension</h2> 
  <p class="ozone">For most adults, there's no identifiable cause of high blood pressure. This type of high blood pressure, called primary (essential) hypertension, tends to develop gradually over many years.</p>
  <h2 class="orange">Secondary hypertension</h2> 
  <p class="ozone">Some people have high blood pressure caused by an underlying condition. This type of high blood pressure, called secondary hypertension, tends to appear suddenly and cause higher blood pressure than does primary hypertension. Various conditions and medications can lead to secondary hypertension, including:</p>
  <ul class="my-list">
  <li>Obstructive sleep apnea</li>
  <li>Kidney disease</li>
  <li>Adrenal gland tumors</li>
  <li>Thyroid problems</li>
  <li>Certain defects you're born with (congenital) in blood vessels</li>
  <li>Certain medications, such as birth control pills, cold remedies, decongestants, over-the-counter pain relievers and some prescription drugs</li>
  <li>Illegal drugs, such as cocaine and amphetamines</li>
</ul> 
